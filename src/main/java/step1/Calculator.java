package step1;

public class Calculator {

    static int addition(int x, int y) {
        return x + y;
    }

    static int subtraction(int x, int y) {
        return x - y;
    }

    static int multiplication(int x, int y) {
        return x * y;
    }

    static int division(int x, int y) {
        return x / y;
    }

}
